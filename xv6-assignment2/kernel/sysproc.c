uint64
sys_sbrk(void)
{
  uint64 addr;
  int t;
  int n;

  argint(0, &n);
  argint(1, &t);
  addr = myproc()->sz;

  if(t == SBRK_EAGER || n < 0) {
    if(growproc(n) < 0) {
      return -1;
    }
  } else {
    // Lazily allocate memory for this process: increase its memory
    // size but don't allocate memory. If the processes uses the
    // memory, vmfault() will allocate it.
    if(addr + n < addr)
      return -1;
    if(addr + n > TRAPFRAME)
      return -1;
    myproc()->sz += n;
  }
  return addr;
}

uint64
sys_pause(void)
{
  int n;
  uint ticks0;

  argint(0, &n);
  if(n < 0)
    n = 0;
  acquire(&tickslock);
  ticks0 = ticks;
  while(ticks - ticks0 < n){
    if(killed(myproc())){
      release(&tickslock);
      return -1;
    }
    sleep(&ticks, &tickslock);
  }
  release(&tickslock);
  return 0;
}

uint64
sys_kill(void)
{
  int pid;

  argint(0, &pid);
  return kkill(pid);
}

// return how many clock tick interrupts have occurred
// since start.
uint64
sys_uptime(void)
{
  uint xticks;

  acquire(&tickslock);
  xticks = ticks;
  release(&tickslock);
  return xticks;
}


// 1. Define the global randomness state and the lock
static uint random_state = 1;
struct spinlock rand_lock;

// 2. Implement the kernel functions
void lcg_srand(uint seed) {
  acquire(&rand_lock);
  random_state = seed;
  release(&rand_lock);
}

uint lcg_rand(void) {
  uint a = 1664525;
  uint b = 1013904223;
  uint current_val;

  acquire(&rand_lock);
  // Unsigned 32-bit integer overflow automatically handles the modulo 2^32
  random_state = a * random_state + b;
  current_val = random_state;
  release(&rand_lock);

  return current_val;
}

uint64 sys_lcg_srand(void) {
  int seed;
  // Just fetch the argument directly. No need to check for < 0!
  argint(0, &seed);
  
  lcg_srand((uint)seed);
  return 0; 
}

uint64 sys_lcg_rand(void) {
  return lcg_rand(); 
}